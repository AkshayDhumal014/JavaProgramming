package com.java.conditional_programs;

public class SwapNumber {

	public static void main(String[] args) {
        int a=10,b=20;
         int t;
         t=a;
         a=b;
         b=t;
        System.out.println("a="+a);
        System.out.println("b="+b);
		
	
        System.out.println("-----------");
        int x=15,y=25;
          x=x+y;
          y=x-y;
          x=x-y;
        System.out.println("x="+x);
        System.out.println("y="+y);
	}

}
